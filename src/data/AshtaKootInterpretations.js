/**
 * Get interpretation for a specific koota based on score and parihar status
 */
export const getKootaInterpretation = (kootaName, points, maxPoints, hasParihar, hasDosha) => {
  const interpretation = KOOTA_INTERPRETATIONS[kootaName];
  
  if (!interpretation) {
    return null;
  }

  let interpretationType = '';
  let interpretationText = '';
  let threshold = '';

  if (hasParihar) {
    interpretationType = 'parihar';
    interpretationText = interpretation.parihar;
    threshold = 'Parihar';
  } else if (points === maxPoints) {
    interpretationType = 'maxScore';
    interpretationText = interpretation.maxScore.text;
    threshold = interpretation.maxScore.threshold;
  } else if (hasDosha || points === 0) {
    interpretationType = 'zeroScore';
    interpretationText = interpretation.zeroScore?.text || interpretation.lowScore?.text || '';
    threshold = interpretation.zeroScore?.threshold || interpretation.lowScore?.threshold || '0/' + maxPoints;
  } else if (interpretation.lowScore && points < maxPoints / 2) {
    interpretationType = 'lowScore';
    interpretationText = interpretation.lowScore.text;
    threshold = interpretation.lowScore.threshold;
  } else {
    interpretationType = 'partial';
    interpretationText = null;
    threshold = points + '/' + maxPoints;
  }

  return {
    ...interpretation,
    interpretationType,
    interpretationText,
    threshold,
    points,
    maxPoints
  };
};

/**
 * Get full interpretation for all koota results
 */
export const getFullInterpretation = (kootaResults) => {
  return kootaResults.map(koota => {
    const hasParihar = koota.parihar?.active || false;
    const hasDosha = koota.dosha !== null && koota.dosha !== undefined;
    
    return {
      ...koota,
      interpretation: getKootaInterpretation(
        koota.name,
        koota.points,
        koota.maxPoints,
        hasParihar,
        hasDosha
      )
    };
  });
};

